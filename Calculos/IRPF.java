package Calculos;

public class IRPF {
    public double CalcIRPF1(double sal){
        
        double descIrpf1 = sal * 0.075;
        
        return descIrpf1;
    }
    public double CalcIRPF2(double sal){
        
        double descIrpf1 = sal * 0.15;
        
        return descIrpf1;
    }
    public double CalcIRPF3(double sal){
        
        double descIrpf1 = sal * 0.225;
        
        return descIrpf1;
    }
    public double CalcIRPF4(double sal){
        
        double descIrpf1 = sal * 0.275;
        
        return descIrpf1;
    }
}
