package Calculos;

public class INSS {

    public double CalcINSSd1(double sal){
        
        double desc1 = sal * 0.075;
                
        return desc1;
    }
    public double CalcINSSd2(double sal){
        
        double desc1 = sal * 0.09;
        
        return desc1;
    }
    public double CalcINSSd3(double sal){
        
        double desc1 = sal * 0.12;
        
        return desc1;
    }
    public double CalcINSSd4(double sal){
        
        double desc1 = sal * 0.14;
        
        return desc1;
    }
}
